    public void display(ArrayList<Buku> bukuList) {
        Object[] kolom = { "Judul" };
        DefaultTableModel model = new DefaultTableModel(kolom, 0);
        
        for(Buku buku : bukuList) {
            Object[] baris = { buku.judul };
            model.addRow(baris);
        }
        
        jtBuku.setModel(model);
    }
//    untuk memilih buku
    public void tambahBuku(Buku buku, int lama){
        String judul = buku.judul(); //masukan judul yg di ambil ke variabel judul
        BukuDipinjam bukuDipinjam = new BukuDipinjam(judul, lama);//buat objek nya
        
        this.bukuDipinjamCollection.add(bukuDipinjam);
    }
    
//    untuk tampil pinjaman ke tabel pinjaman
    public void tampilPinjaman(ArrayList<BukuDipinjam> Buku){
        Object[] kolom = { "Judul" };
        DefaultTableModel model = new DefaultTableModel(kolom, 0);
        
        for(BukuDipinjam buku : Buku) {
            Object[] baris = { buku.judul() };
            model.addRow(baris);
        }
        
        jtPeminjaman.setModel(model);
    }

    
//    hapus buku dari tabel pinjaman
    public void hapusBuku(Buku buku){
        for(BukuDipinjam listBuku : this.bukuDipinjamCollection){
            if(listBuku.judul().equalsIgnoreCase(buku.judul())){ //equals ignorecase untuk membaca huruf besar kecil
                this.bukuDipinjamCollection.remove(listBuku);
                break;
            }
        }
        this.tampilPinjaman(bukuDipinjamCollection);
    }
    
