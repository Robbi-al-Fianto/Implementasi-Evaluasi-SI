    private void jButtonPinjamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonPinjamMouseClicked
        // TODO add your handling code here:
        
             String lama = jtFieldLama.getText();
        
        if(lama.isEmpty() || lama.equals("0")){
            DialogUI dialogUI = new DialogUI("Masukkan Durasi Peminjaman");
            dialogUI.pack();
            dialogUI.setLocationRelativeTo(null);
            dialogUI.setVisible(true);  
        } else if(!lama.isEmpty()){
            // ubah waktu ke int
            int lamaInt = Integer.parseInt(lama);
            
            if(lamaInt>3){
                DialogUI dialogUI = new DialogUI("Peminjaman buku maksimal 3 hari");
                dialogUI.pack();
                dialogUI.setLocationRelativeTo(null);
                dialogUI.setVisible(true); 
            } else {
                // inisisai pengambilan judul buku dari tabel daftar buku
                int row = jtBuku.getSelectedRow();
                int column = jtBuku.getSelectedColumn();
                String judul = jtBuku.getModel().getValueAt(row, column).toString();
                
                this.tambahBuku(new Buku(judul), lamaInt); // tambah buku
                this.tampilPinjaman(this.bukuDipinjamCollection);
                
            }
        }
    }//GEN-LAST:event_jButtonPinjamMouseClicked

    private void jButtonBatalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonBatalMouseClicked
        // TODO add your handling code here:
        
        
                // inisiasi pengambilan judul buku
        int row = jtPeminjaman.getSelectedRow();
        int column = jtPeminjaman.getSelectedColumn();
        String judul = jtPeminjaman.getModel().getValueAt(row, column).toString();

        String lama = jtFieldLama.getText();
        
        if(lama.isBlank()){
                DialogUI dialogUI = new DialogUI("Peminjaman buku maksimal 3 hari");
                dialogUI.pack();
                dialogUI.setLocationRelativeTo(null);
                dialogUI.setVisible(true); }
        
        this.hapusBuku(new Buku (judul));
        
    }//GEN-LAST:event_jButtonBatalMouseClicked

